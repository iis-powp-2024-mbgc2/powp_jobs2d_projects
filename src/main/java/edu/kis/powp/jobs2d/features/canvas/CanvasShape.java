package edu.kis.powp.jobs2d.features.canvas;

public class CanvasShape {
    private final int x, y, width, height;

    public CanvasShape(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
